#SLICING 
# - string datatype is immutable
# - cannot delete single character 
# - can delete whole string.
# - [ ] , [ : ] , [ : : ]

str = 'ITM skills university'

print(str[0:3]) # output = ITM
print(str[-21:-18]) # output = ITM
print(str[0:3:1]) # OUTPUT  = ITM
print(str[0:3:2]) # output = IM
print(str[ : :-1]) # output = ytisrevinu slliks MTI

